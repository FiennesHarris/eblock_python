'''
add a for loop that will print the following output

Wife 1 was Aragon
Wife 2 was Boleyn
Wife 3 was Seymour
Wife 4 was Cleves
Wife 5 was Howard
Wife 6 was Parr
'''
Wives =["Aragon","Boleyn","Seymour","Cleves","Howard","Parr"]
num = 0

for i in (Wives):
    num = num +1
    print(f"wife {num} was {i}")

